// абсолютное значение (модуль) - Math.abs()

// №1⊗jsPmSMMA
// Даны переменные a и b.
// Найдите модуль разности a и b.
// Проверьте работу скрипта самостоятельно для
// различных a и b.

function absoluteDifference(a, b) {
  return Math.abs(a - b);
}

console.log(absoluteDifference(2, 120));
console.log(absoluteDifference(89, 91));
