// Проверьте, что переменная num больше нуля и
// меньше 5.

let num = 3;
if (num > 0 && num < 5) {
  console.log("переменная num больше нуля и меньше 5.");
} else {
  console.log("переменная num не больше нуля и меньше 5.");
}

