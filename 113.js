// Проверьте, что переменная num больше или равна 10
// и меньше или равна 20.

if (num >= 10 && num <= 20) {
  console.log("num больше или равна 10 и меньше или равна 20.");
} else {
  console.log("num не больше или равна 10 и меньше или равна 20.");
}

