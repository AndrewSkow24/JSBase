// Дан массив с числами. Запустите цикл, который будет по очереди выводить элементы этого массива в консоль до
// тех пор, пока не встретится элемент со значением 0. После этого цикл должен завершить свою работу.
console.log("task 1");
let myArr = [1, 2, 3, 113, 123, 12, 126, 8219, 123, 0, 12, 11, 56, 423, 4234];
for (let elem of myArr) {
  if (elem == 0) {
    console.log("0 обнаружен в массиве");
    break;
  }

  console.log(elem);
}

