// Определите, сколько целых чисел, начиная с числа 1, нужно сложить, чтобы сумма получилась больше 100.
console.log("task 4");
sum = 0;
let i = 1; // первая цифра

while (sum < 100) {
  sum += i;
  i++;
  console.log("Число: ", i);
  console.log("Текущая сумма", sum);
}
