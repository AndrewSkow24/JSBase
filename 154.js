// Дан массив с числами. Найдите сумму элементов,
// расположенных от начала массива до первого отрицательного числа.
console.log("task 2");
let arrTask2 = [892, 232, 12, -1, 21, 12, 12];
let sum = 0;
for (let i = 0; i <= arrTask2.length - 1; i++) {
  if (arrTask2[i] < 0) {
    console.log("обнаружено отрицательное число - выход из цикла");
    break;
  }
  sum += arrTask2[i];
}
console.log(sum);

