// Передача объектов по ссылке в JavaScript

/* 

Массивы и объекты несколько отличаются от остальных 
примитивных типов данных. Отличие в том, что переменная, 
хранящая массив, на самом деле не содержит его, а 
просто ссылается на него. 

На практике это означает, что при записи объекта
в другую переменную, обе будут ссылаться на один
и тот же объект. 
*/

