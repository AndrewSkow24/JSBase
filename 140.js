// Как известно, четные числа делятся на 2 без остатка,
//  а нечетные - с остатком. Пусть у вас дано число.
// С помощью оператора % и конструкции if проверьте
// четное это число или нет.

function isEven(num) {
  num % 2 == 0 ? console.log("Чётное число") : console.log("Нечётное число");
}
isEven(2);
isEven(125);
isEven(124);

