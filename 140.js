// Дано число. Проверьте, что оно делится на 3.

function division3(num) {
  num % 3 == 0
    ? console.log("Число делится на 3 без остатка")
    : console.log("Число не делится на 3 без остатка");
}

division3(123);
division3(3);
division3(7);
